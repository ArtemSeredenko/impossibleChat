import React, { useEffect, useRef, useState } from "react";
import "./ChatBot.css";

type Message = {
  id: string;
  from: "user" | "bot";
  text: string;
  ts: number;
};

const MAKE_WEBHOOK_URL =
  "https://hook.eu2.make.com/3avlu7axojiz4ausryvj5sexnwhigvh6";

function ChatBot() {
  const [messages, setMessages] = useState<Message[]>([
    {
      id: crypto.randomUUID(),
      from: "bot",
      text: "–ü—Ä–∏–≤—ñ—Ç üëã –Ø –æ–Ω–ª–∞–π–Ω-–∞—Å–∏—Å—Ç–µ–Ω—Ç. –ü–∏—Ç–∞–π —â–æ –∑–∞–≤–≥–æ–¥–Ω–æ.",
      ts: Date.now(),
    },
  ]);
  const [input, setInput] = useState<string>("");
  const [sending, setSending] = useState<boolean>(false);
  const listRef = useRef<HTMLDivElement>(null);

  // –∞–≤—Ç–æ—Å–∫—Ä–æ–ª–ª –≤–Ω–∏–∑ –ø—Ä–∏ –Ω–æ–≤—ã—Ö —Å–æ–æ–±—â–µ–Ω–∏—è—Ö
  useEffect(() => {
    const el = listRef.current;
    if (!el) return;
    el.scrollTo({ top: el.scrollHeight, behavior: "smooth" });
  }, [messages]);

  const sendMessage = async () => {
    const text = input.trim();
    if (!text || sending) return;

    const userMsg: Message = {
      id: crypto.randomUUID(),
      from: "user",
      text,
      ts: Date.now(),
    };

    setMessages((prev) => [...prev, userMsg]);
    setInput("");
    setSending(true);

    try {
      const res = await fetch(MAKE_WEBHOOK_URL, {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        // –ø—Ä–∏ –∂–µ–ª–∞–Ω–∏–∏ –¥–æ–±–∞–≤—å userId/sessionId –≤ payload
        body: JSON.stringify({ message: text }),
      });

      const data: { reply?: string } = await res.json().catch(() => ({}));
      const replyText =
        (typeof data?.reply === "string" && data.reply.trim()) ||
        (res.ok
          ? "–Ø –ø–æ—á—É–≤(–ª–∞) —Ç–µ–±–µ, –∞–ª–µ —â–æ—Å—å –ø—ñ—à–ª–æ –Ω–µ —Ç–∞–∫ —ñ–∑ –≤—ñ–¥–ø–æ–≤—ñ–¥–¥—é ü§î"
          : `–°–µ—Ä–≤–µ—Ä –≤—ñ–¥–ø–æ–≤—ñ–≤ –ø–æ–º–∏–ª–∫–æ—é ${res.status}`);

      const botMsg: Message = {
        id: crypto.randomUUID(),
        from: "bot",
        text: replyText,
        ts: Date.now(),
      };

      setMessages((prev) => [...prev, botMsg]);
    } catch {
      setMessages((prev) => [
        ...prev,
        {
          id: crypto.randomUUID(),
          from: "bot",
          text: "‚ö†Ô∏è –ù–µ–º–∞ –∑–≤'—è–∑–∫—É –∑ –±–æ—Ç–æ–º. –ü–µ—Ä–µ–≤—ñ—Ä Make –∞–±–æ –º–µ—Ä–µ–∂—É.",
          ts: Date.now(),
        },
      ]);
    } finally {
      setSending(false);
    }
  };

  const handleKeyDown: React.KeyboardEventHandler<HTMLInputElement> = (e) => {
    if (e.key === "Enter" && !e.shiftKey) {
      e.preventDefault();
      sendMessage();
    }
  };

  return (
    <div className="chat-container" role="region" aria-label="–û–Ω–ª–∞–π–Ω-—á–∞—Ç">
      <div ref={listRef} className="chat-messages">
        {messages.map((m) => (
          <div
            key={m.id}
            className={`chat-message ${m.from}`}
            title={new Date(m.ts).toLocaleString()}
          >
            {m.text}
          </div>
        ))}
      </div>

      <div className="chat-input">
        <input
          type="text"
          value={input}
          onChange={(e) => setInput(e.target.value)}
          onKeyDown={handleKeyDown}
          placeholder={sending ? "–ë–æ—Ç –¥—Ä—É–∫—É—î..." : "–ù–∞–ø–∏—à–∏ –ø–æ–≤—ñ–¥–æ–º–ª–µ–Ω–Ω—è..."}
          disabled={sending}
          aria-label="–ü–æ–ª–µ –≤–≤–æ–¥–∞ —Å–æ–æ–±—â–µ–Ω–∏—è"
        />
        <button
          onClick={sendMessage}
          disabled={sending}
          aria-label="–û—Ç–ø—Ä–∞–≤–∏—Ç—å —Å–æ–æ–±—â–µ–Ω–∏–µ"
          title="Enter ‚Äî –æ—Ç–ø—Ä–∞–≤–∏—Ç—å"
        >
          ‚û§
        </button>
      </div>

      <div className="chat-hint">
        –ü–æ—Ä–∞–¥–∞: –Ω–∞—Ç–∏—Å–Ω–∏ <kbd>Enter</kbd>, —â–æ–± –≤—ñ–¥–ø—Ä–∞–≤–∏—Ç–∏.
      </div>
    </div>
  );
}
export default ChatBot;